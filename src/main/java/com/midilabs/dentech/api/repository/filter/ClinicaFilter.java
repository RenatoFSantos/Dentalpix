public class ClinicaFilter {
	
	private String nome;
	private String documento;
	
	// *********************************************
	// --- GETs, SETs
	// *********************************************

	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDocumento() {
		return documento;
	}
	public void setDocumento(String documento) {
		this.documento = documento;
	}

}
